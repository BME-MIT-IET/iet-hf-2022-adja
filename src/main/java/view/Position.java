package view;

/**
 * 2D Pozíciót/méretet reprezentáló osztály
 */
public class Position {

    /**
     * Pozíció
     */
    public int x, y;

    /**
     * Konstruktor
     * @param x: x koordináta
     * @param y: y koordináta
     */
    public Position(int x, int y){
        this.x = x;
        this.y = y;
    }
}
